def main() -> None:
    """Main CLI entry point for the ``apriori`` command."""
    parser = _build_parser()
    args = parser.parse_args()

    if args.command == "init":
        _cmd_init(args)
    elif args.command == "search":
        _cmd_search(args)
    elif args.command == "status":
        _cmd_status(args)
    elif args.command == "rebuild-index":
        _cmd_rebuild_index(args)
    elif args.command == "config":
        _cmd_config(args)
    elif args.command == "ui":
        _cmd_ui(args)
    elif args.command == "blast-radius":
        _cmd_blast_radius(args)
    elif args.command == "librarian":
        sub = getattr(args, "librarian_subcommand", None)
        if sub == "run":
            _cmd_librarian_run(args)
        elif sub == "status":
            _cmd_librarian_status(args)
        else:
            # Print librarian sub-help
            parser._subparsers._group_actions[0].choices["librarian"].print_help()
            sys.exit(0)
    elif args.command == "concept":
        _cmd_concept(args)
    elif args.command == "validate":
        _cmd_validate(args)
    elif args.command == "export":
        _cmd_export(args)
    elif args.command == "doctor":
        _cmd_doctor(args)
    else:
        parser.print_help()
        sys.exit(0)


if __name__ == "__main__":
    main()
