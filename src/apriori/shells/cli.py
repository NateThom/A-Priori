def _resolve_db_path(args: argparse.Namespace) -> Path:
    """Determine the SQLite database path from args, config, or defaults.

    Resolution order:
    1. Explicit ``--db`` argument.
    2. Config file found at ``.apriori/apriori.config.yaml``.
    3. Fallback: ``.apriori/graph.db``.
    """
    if args.db:
        return Path(args.db)

    from apriori.config import load_config

    apriori_config = Path.cwd() / ".apriori" / "apriori.config.yaml"
    if apriori_config.exists():
        config = load_config(apriori_config)
    else:
        config = load_config()
    return Path(config.storage.sqlite_path)


